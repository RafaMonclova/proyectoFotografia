    private void botonPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonPagarActionPerformed
        
        ClienteBDD clientes = new ClienteBDD();
        FacturaBDD facturas = new FacturaBDD();
        CompraBDD compras = new CompraBDD();
        
        double importeTotal = 0;
        
        ArrayList<Compra> listaCompras = compras.readAll();
        
        for (int i = 0; i < listaCompras.size(); i++) {
            
            importeTotal += listaCompras.get(i).getPrecio();
        }
       
            //Crea la factura con el DNI seleccionado en el ComboBox y el importe total de las compras
        Factura f = new Factura(elegirCliente.getSelectedItem().toString(),importeTotal);
        f.setCompras(listaCompras);
        facturas.insert(f);
        
        tbl = (DefaultTableModel)jTable1.getModel();
        Object[] row = { f.getCodigo(), f.getDni(), f.modelosLista(), f.getImporteTotal() };
        tbl.addRow(row);
        
        Cliente pagador = clientes.read(elegirCliente.getSelectedItem().toString());
        panel.add(new CargarImagenCliente(pagador.getDni()));
        
        //Se borran las compras de la base de datos y la lista
        for (int i = 0; i < listaCompras.size(); i++) {
            
            compras.delete(listaCompras.get(i).getCodigo());
            
        }
        listaCompras.removeAll(listaCompras);
        
        
    }//GEN-LAST:event_botonPagarActionPerformed

    public void elegirCliente(){
        
        elegirCliente.removeAllItems();
        
        ClienteBDD clientes = new ClienteBDD();
        
        
        
        ArrayList<Cliente> listaClientes = new ArrayList();
        listaClientes = clientes.readAll();
            
            for (int i = 0; i < listaClientes.size(); i++) {
                
                elegirCliente.addItem(listaClientes.get(i).getDni());
                
            }
     
        
        
    }
    
    
